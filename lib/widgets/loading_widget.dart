class VenueListShimmer extends StatelessWidget {
  final int itemCount;

  const VenueListShimmer({
    super.key,
    this.itemCount = 5,
  });

  @override
  Widget build(BuildContext context) {
    return ListView.builder(
      itemCount: itemCount,
      itemBuilder: (context, index) => const VenueCardShimmer(),
    );
  }
}
