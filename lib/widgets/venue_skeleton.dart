    super.key,
    this.itemCount = 5,
  });

  @override
  Widget build(BuildContext context) {
    return Expanded(
      child: ListView.builder(
        itemCount: itemCount,
        itemBuilder: (context, index) => const VenueSkeletonCard(),
      ),
    );
  }
}

